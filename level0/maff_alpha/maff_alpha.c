#include <unistd.h>

int main(void)
{
	int		i;
	char	temp;

	i = 97;
	while(i <= 122)
	{
		if(i % 2 == 0)
		{
			temp = i - 32;
			write(1, &temp, 1);
			i++;
			continue;
		}
			temp = i;
			write(1, &temp, 1);
		i++;
	}
	write(1, "\n", 1);
}